//-------- storing places in array

let places : string[] =  ['Wonderland', 'Dancing fountain', 'Statue of Liberty' ,'London bridge'];

//-----------print array in its original form
console.log (' Original ' + places);

//-------- print array in alphabetical order without changing/ here... means copy of array.
console.log (' Copy ' + [...places].sort());

//-------------showing array in its original form
console.log (' Original ' + places);

//----------array in reverse alphabetical order
console.log (' Copy ' + [...places].sort().reverse());

// ------ showing array in its oiginal form
console.log (' Original ' + places);

//---- reverse order of list
console.log (' Original ' + places.reverse());

//-----------reverse the array again
console.log (' Original ' + places.reverse());

//---------sort array in alphabetical order
console.log (' Originl ' + places.sort());

